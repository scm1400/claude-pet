/**
 * Returns a message for the given mood and locale, stable within the same 2-minute window.
 */
export function getMessage(mood: PetMoodKey, locale: Locale = DEFAULT_LOCALE): string {
  const pool = MESSAGE_POOLS[locale]?.[mood] ?? MESSAGE_POOLS.en[mood];
  const windowSeed = Math.floor(Date.now() / 120_000);
  const index = windowSeed % pool.length;
  return pool[index];
}

/**
 * Returns the common quote ID for the message currently displayed.
 * Uses the same time-seeded index as getMessage().
 */
export function getCurrentCommonQuoteId(mood: PetMoodKey): string {
  const pool = MESSAGE_POOLS.ko[mood];
  const windowSeed = Math.floor(Date.now() / 120_000);
  const index = windowSeed % pool.length;
  return `common_${mood}_${index}`;
}

export { MESSAGE_POOLS };
